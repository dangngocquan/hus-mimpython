# Trong meme có hai đối tượng, theo đó là hai cách xử lí khác nhau khi gặp vực thẳm:
#     + Con công (có lẽ vậy) tượng trưng cho vòng lặp while, nó sẽ kiểm tra xem phía 
#     trước có vực thẳm không, nếu không có thì sẽ tiếp tục chạy
#     + Con cáo tượng trưng cho vòng lặp do-while, nó sẽ chạy trước, sau đó mới 
#     kiểm tra xem có vực thẳm phía trước không
# Hai cách xử lí của hai con vật này, cũng tượng trưng cho cách hoạt động của vong lặp while và do-while:
#     + Vòng lặp while sẽ kiểm tra điều kiện trước, nếu đúng thì mới thực hiện khối lệnh bên trong
#     + Vòng lặp do_while thì thực hiện khối lệnh ít nhất 1 lần, sau đó mới kiểm tra điều kiện. 
# Mỗi loại đều có lợi trong những trường hợp khác nhau. Và trong trường hợp ở meme này, thì do-while 
# sẽ khiến con cáo ngỏm ~ (cũng có nghĩa là không nên dùng do-while trong trường hợp này)